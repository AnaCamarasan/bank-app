    private double bal;
    private SavingsAccount acc;

    @BeforeEach
    public void setUp() {
        bal = 1999.99;
        acc = new SavingsAccount(
                bal,
                100,
                "Hott Toddy",
                2.56
        );
    }

    @Test
    public void testSavingsAccountWithdrawal() {
        assertEquals(bal, acc.getAccountBalance());
        acc.withdraw(100);
        assertEquals(bal - 100, acc.getAccountBalance());
    }

    @Test
    public void testGetANDSetInterestRate() {
        double r = 7.11;
        assertEquals(2.56, acc.getInterestRate());
        acc.setInterestRate(r);
        assertEquals(r, acc.getInterestRate());
    }

    @Test
    public void testToString() {
        System.out.println(acc.toString());
    }

    @Test
    public void testEstimateYearlyCompoundInterest() {

        acc.setAccountBalance(100);
        acc.setInterestRate(3.0);
        // 0.002 Error margin
        assertEquals(106.09, acc.estimateYearlyCompoundInterest(2), 0.002);

        acc.setAccountBalance(100);
        acc.setInterestRate(3.0);
        assertEquals(100, acc.estimateYearlyCompoundInterest(0), 0.002);
    }

